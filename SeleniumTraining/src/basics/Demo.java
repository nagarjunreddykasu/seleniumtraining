/*
Naming Conventions:
-------------------
class names:  class name should starts with upper case letter and every inner word should starts with upper case
eg: LoginTest

variable: variable name starts with lower case letter and every inner word should starts with upper case
ex: count, userName

methods: method name starts with lower case letter and every inner word should starts with upper case
ex: click(), getText(), sendKeys()

package:
package name must be defined with lower cases
ex: basics, config, objectrepository

constants:
constant names should be defined with upper case.
ex: PRIORITY





*/