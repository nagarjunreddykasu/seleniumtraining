package basics;

public class IfElse {
	public static void main(String[] args) {
		int a=15;
		if(a>20){
			System.out.println("a value is less than 20");
		}
		else if(a<=15){
			System.out.println("a value is less than or equal to 15");
		}
		else{
			System.out.println("a value is not less than 15 and Not greater than 20");
		}
	}

}
