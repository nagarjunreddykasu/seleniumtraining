package basics;

public class DataTypes {
	
	public static void main(String[] args) {
		
		byte b=10;
		short s=20;
		int i=100;
		long l=200;  //default value for byte, short, int, long is 0
		
		float f=10.75f;
		double d=11.50;  //default value for float, double is 0.0
		
		char ch='K'; //default value for char is space
		boolean bl=true;  //default value for boolean is false
		
		System.out.println("byte value: "+b);
		System.out.println("short value: "+s);
		System.out.println("int value: "+i);
		System.out.println("long value: "+l);
		System.out.println("float value: "+f);
		System.out.println("char value: "+ch);
		System.out.println("boolean value: "+bl);
		
	}

}


